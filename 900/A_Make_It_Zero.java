import java.util.*;

public class A_Make_It_Zero {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int t = sc.nextInt();
    while(t-- > 0) {
      int n = sc.nextInt();
      System.out.println(2);
      System.out.println("1 " + n);
      System.out.println("1 " + n);
    }
    sc.close();
  }
}
